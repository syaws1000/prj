package javaexp.a01_begin;

public class A05_ObjectSecond {  // 클래스이름 선언과 {}(중괄호)로 해당 범위를 지정한다.

	public static void main(String[] args) { 
		// 클래스 내부에 포함된 메서드명으로 이것도 {} 범위를 지정한다.
		// 클래스와 메서드는 포함 관계로 설정이 된다.
		// 한줄 주석을 처리할 때, // 이후에 설명 내용을 넣는다. 수행보다는 코드에 대한 설명을 넣는다.
		/*
		여러줄 주석을 처리할 때, 해당 * * 사이에 넣는다.
		 */
		// 실행문 : 
		// 1. 변수 선언, 값 저장, 메서드 호출에 해당 하는 코드로
		// 2. 실행문 끝에는 반드시 세미콜론(;)을 붙여 실행문의 끝을 표시한다.
		System.out.println("자바 기본 출력 완료!");
		
	}

}
