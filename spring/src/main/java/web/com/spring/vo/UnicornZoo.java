package web.com.spring.vo;

public class UnicornZoo {
	// 입력 : unicornName unicornSpecialAbility
	// 출력 : unicornId unicornName unicornSpecialAbility
	private int unicornId;
	private String unicornName;
	private String unicornSpecialAbility;
	public int getUnicornId() {
		return unicornId;
	}
	public void setUnicornId(int unicornId) {
		this.unicornId = unicornId;
	}
	public String getUnicornName() {
		return unicornName;
	}
	public void setUnicornName(String unicornName) {
		this.unicornName = unicornName;
	}
	public String getUnicornSpecialAbility() {
		return unicornSpecialAbility;
	}
	public void setUnicornSpecialAbility(String unicornSpecialAbility) {
		this.unicornSpecialAbility = unicornSpecialAbility;
	}
	
}
