package web.com.spring.a07_mvc.vo;

public class ImaginaryAnimal {
	
	// animalId animalName animalAbility habitat
	private int animalId;
	private String animalName;
	private String animalAbility;
	private String habitat;
	public int getAnimalId() {
		return animalId;
	}
	public void setAnimalId(int animalId) {
		this.animalId = animalId;
	}
	public String getAnimalName() {
		return animalName;
	}
	public void setAnimalName(String animalName) {
		this.animalName = animalName;
	}
	public String getAnimalAbility() {
		return animalAbility;
	}
	public void setAnimalAbility(String animalAbility) {
		this.animalAbility = animalAbility;
	}
	public String getHabitat() {
		return habitat;
	}
	public void setHabitat(String habitat) {
		this.habitat = habitat;
	}
	
}
